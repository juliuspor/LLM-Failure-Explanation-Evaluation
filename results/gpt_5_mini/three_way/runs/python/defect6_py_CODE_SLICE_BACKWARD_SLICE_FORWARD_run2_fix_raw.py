def add_number(self, x: float):
    # This is not pretty printing. This is to prevent misparsing of x- -4 as
    # x--4 (which is a syntax error).
    prev = self.get_last_char()
    if x < 0 and prev == '-':
        self.add(" ")

    if x == int(x) and not math.isinf(x) and not math.isnan(x):
        value = int(x)
        mantissa = value
        exp = 0
        if abs(x) >= 100:
            # Try to factor powers of 10 out of the integer value to create
            # a compact scientific-style representation mantissa * 10**exp == value
            while mantissa % 10 == 0 and mantissa != 0:
                # Check that dividing mantissa by 10 and increasing exp keeps equality
                if (mantissa // 10) * (10 ** (exp + 1)) == value:
                    mantissa //= 10
                    exp += 1
                else:
                    break

        if exp > 2:
            self.add(str(mantissa) + "E" + str(exp))
        else:
            self.add(str(value))
    else:
        self.add(str(x))